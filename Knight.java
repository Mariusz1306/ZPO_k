public class Knight { //w klasie knight przechowujemy obecną pozycję konia
    public int x;
    public int y;

    public Knight(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
